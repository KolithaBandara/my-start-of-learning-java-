class Example0024{
  public static  void main(String []args){
		System.out.println("ABCD"); //String Literal
		System.out.println("AB\bCD"); //String Literal ---> But using "\b" (Backspace Character) to backspace B
		System.out.println("AB\tCD"); //String Literal ---> But, using "\t" to insert a new tab 
		System.out.println("AB\nCD"); //String Literal ---> But, using "\n" to insert a new line
	}
}
