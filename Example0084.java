import java.util.*; 
class Example0084{
	public static  void main(String []args){
		int x=100;
		System.out.println(x); //100
		
		x++; //x, increment by 1
		System.out.println(x); //101

		++x; //x, increment by 1
		System.out.println(x); //102

		x--; //x, decrement by 1
		System.out.println(x); //101

		--x; //x, decrement by 1
		System.out.println(x); //100  
	}
}
