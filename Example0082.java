import java.util.*; 
class Example{
	public static  void main(String []args){
		int x=100,y=50,z;
		z=x+y;
		System.out.println(x+" + "+y+" = "+z); //100 + 50 = 150

		z=x-y;
		System.out.println(x+" - "+y+" = "+z); //100 - 50 = 50

		z=x*y;
		System.out.println(x+" * "+y+" = "+z); //100 * 50 = 5000

		z=x/y;
		System.out.println(x+" / "+y+" = "+z); //100 / 50 = 2
	}
}
