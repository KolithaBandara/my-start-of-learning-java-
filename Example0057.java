class Example0057{
	public static  void main(String []args){
		int x=10,y=20; //declaring same data type variables in single line
		System.out.println("x+y"); //x+y
		System.out.println("x"+"y"); //xy
		System.out.println(x+"y"); //10y
		System.out.println("x"+y); //x20
		System.out.println(x+y); //30

	} 
}
